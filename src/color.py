class color:
    """ A class for constants to stylize terminal output. """
    PURPLE = '\033[95m'
    CYAN = '\033[96m'
    DARKCYAN = '\033[36m'
    BLUE = '\033[94m'
    GREEN = '\033[92m'
    YELLOW = '\033[93m'
    RED = '\033[91m'
    GRAY = '\033[37m'
    BOLD = '\033[1m'
    UNDERLINE = '\033[4m'
    FAINT = '\033[2m'
    NOT_FAINT = '\033[22m'
    BLINK = '\033[5m'
    END = '\033[0m'
    PLAIN = END

    COLORS = ['PLAIN', 'GRAY', 'PURPLE', 'CYAN', 'DARKCYAN', 'BLUE', 'GREEN', 'YELLOW', 'RED']
